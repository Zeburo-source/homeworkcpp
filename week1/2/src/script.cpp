#include <iostream>
#include <limits>  // Для получения предельных значений типов данных

int main() {
    // Тип данных: int (целое число)
    std::cout << "Тип данных: int\n";
    std::cout << "Минимальное значение: " << std::numeric_limits<int>::min() << std::endl;
    std::cout << "Максимальное значение: " << std::numeric_limits<int>::max() << std::endl;
    
    // int хранится в 4 байтах (32 бита)
    // Из них 1 бит для знака (положительное или отрицательное число), остальные 31 бит для значения.
    // Минимальное значение: -2^(31) = -2147483648
    // Максимальное значение: 2^(31) - 1 = 2147483647

    // Тип данных: unsigned int (беззнаковое целое число)
    std::cout << "\nТип данных: unsigned int\n";
    std::cout << "Минимальное значение: " << std::numeric_limits<unsigned int>::min() << std::endl;
    std::cout << "Максимальное значение: " << std::numeric_limits<unsigned int>::max() << std::endl;
    
    // unsigned int хранится в 4 байтах (32 бита)
    // Все биты используются для хранения значения (без знака).
    // Минимальное значение: 0
    // Максимальное значение: 2^(32) - 1 = 4294967295

    // Тип данных: long long (целое число с большим диапазоном)
    std::cout << "\nТип данных: long long\n";
    std::cout << "Минимальное значение: " << std::numeric_limits<long long>::min() << std::endl;
    std::cout << "Максимальное значение: " << std::numeric_limits<long long>::max() << std::endl;

    // long long хранится в 8 байтах (64 бита)
    // 1 бит для знака, остальные 63 бита для значения.
    // Минимальное значение: -2^(63) = -9223372036854775808
    // Максимальное значение: 2^(63) - 1 = 9223372036854775807

    // Тип данных: float (число с плавающей запятой)
    std::cout << "\nТип данных: float\n";
    std::cout << "Минимальное значение: " << std::numeric_limits<float>::min() << std::endl;
    std::cout << "Максимальное значение: " << std::numeric_limits<float>::max() << std::endl;

    // float хранится в 4 байтах (32 бита)
    // Использует стандарт IEEE 754: 1 бит для знака, 8 бит для экспоненты, и 23 бита для мантиссы.
    // Это дает максимальное значение порядка ~3.4e+38 и минимальное ~1.2e-38 (не включая отрицательные числа).

    // Тип данных: double (число с плавающей запятой двойной точности)
    std::cout << "\nТип данных: double\n";
    std::cout << "Минимальное значение: " << std::numeric_limits<double>::min() << std::endl;
    std::cout << "Максимальное значение: " << std::numeric_limits<double>::max() << std::endl;

    // double хранится в 8 байтах (64 бита)
    // 1 бит для знака, 11 бит для экспоненты, и 52 бита для мантиссы.
    // Это дает максимальное значение порядка ~1.7e+308 и минимальное ~2.2e-308.

    return 0;
}
